  let locale = DEFAULT_LOCALE;

  function t(key) {
    const pack = MESSAGES[locale];
    if (pack && Object.prototype.hasOwnProperty.call(pack, key)) {
      return pack[key];
    }
    return MESSAGES.en[key] ?? MESSAGES.zh[key] ?? key;
  }

  function tf(key, ...args) {
    let s = t(key);
    for (let i = 0; i < args.length; i += 1) {
      s = s.split(`{${i}}`).join(String(args[i]));
    }
    return s;
  }

  function getLocale() {
    return locale;
  }

  function setLocale(id) {
    locale = normalizeLocale(id);
  }

  function applyDocumentMeta() {
    document.documentElement.lang = htmlLangAttrForLocale(locale);
    document.title = t("docTitle");
  }

  function applyDomI18n() {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const k = el.getAttribute("data-i18n");
      if (k) el.textContent = t(k);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const k = el.getAttribute("data-i18n-placeholder");
      if (k && "placeholder" in el) el.placeholder = t(k);
    });
    document.querySelectorAll("[data-i18n-title]").forEach((el) => {
      const k = el.getAttribute("data-i18n-title");
      if (k) el.title = t(k);
    });
    document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      const k = el.getAttribute("data-i18n-aria");
      if (k) el.setAttribute("aria-label", t(k));
    });
  }

  function rebuildLangSelect(selectEl) {
    if (!selectEl) return;
    selectEl.innerHTML = "";
    for (let i = 0; i < SUPPORTED_LOCALES.length; i += 1) {
      const id = SUPPORTED_LOCALES[i];
      const o = document.createElement("option");
      o.value = id;
      o.textContent = t("langLabel_" + id);
      selectEl.appendChild(o);
    }
    locale = normalizeLocale(locale);
    selectEl.value = locale;
  }

  function initFromStorage() {
    try {
      const raw = localStorage.getItem(LS_LANG);
      if (raw) locale = normalizeLocale(raw);
    } catch (e) {
      /* ignore */
    }
    locale = normalizeLocale(locale);
    return locale;
  }

  function persistLocale(id) {
    setLocale(id);
    try {
      localStorage.setItem(LS_LANG, locale);
    } catch (e) {
      /* ignore */
    }
  }

